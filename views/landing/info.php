        <h3>Finalidad del estudio</h3>
        <p>
            Este estudio investiga el <b>papel que juegan las instituciones</b> en el sistema de innovación en los sectores relacionados con la <b>educación superior</b>, la <b>I+D</b> y la promoción de la <b>innovación</b>. El propósito es obtener una serie de conocimientos sobre el funcionamiento de un campo de actividad a través de sus instituciones: universidades, centros públicos y semipúblicos de investigación, centros tecnológicos y otros organismos especializados. En ellas se estudian las <b>relaciones entre gobernanza, valores y resultados</b>.
        </p>

        <h5><b>¿Qué es la Calidad Institucional?</b></h5>
        <p>
            La Calidad Institucional se refiere habitualmente a las <b>prácticas establecidas</b> que gobiernan el <b>funcionamiento</b> de las instituciones y que influyen en el <b>cumplimiento</b> de sus <b>funciones</b> y en sus <b>contribuciones</b> sociales o económicas.
        </p>
        <p>
            En las ciencias sociales existe evidencia creciente sobre la <b>relación entre la calidad institucional y las contribuciones</b> a la sociedad en numerosos sectores, tales como el crecimiento de la <b>economía</b>, la mejora en la prestación de <b>servicios públicos</b> y el nivel de <b>bienestar de la ciudadanía</b>.
        </p>

        <h5><b>¿Por qué es importante estudiar la calidad institucional en el sistema de universidades e I+D?</b></h5>
        <p>
            Las instituciones se consideran <b>componentes fundamentales</b> de los sistemas de investigación y educación superior debido a que condicionan la capacidad de los países y regiones para generar y utilizar conocimiento. Pueden funcionar como barreras, pero también como <b>catalizadores que promueven la innovación y el bienestar</b>.
        </p>
        <p>
            Existe una diversidad de aproximaciones al estudio de la calidad institucional. El término abarca una variedad de definiciones referidas a aspectos tanto formales como informales, desde los incentivos económicos creados por las regulaciones, hasta los rasgos relacionales y culturales de las organizaciones.
        </p>
        <p>
            En este estudio consideramos a las instituciones como "organizaciones complejas infundidas por valores". Tenemos en cuenta sus regulaciones y los aspectos organizativos formales e informales.
        </p>

        <h5><b>Objetivos</b></h5>
        <p>
            El programa de investigación se dirige a obtener una <b>evaluación de las características de las instituciones que contribuyen a un mejor desempeño de sus funciones</b>.
        </p>
        <ul>
            <li>Proporcionar un análisis fundamentado de la estructura interna, los modos de operación y los resultados de las principales instituciones del sistema español de innovación.</li>
            <li>Observar dimensiones o elementos estratégicos de la Calidad Institucional que son susceptibles de influir en los resultados de estas instituciones, tales como las prácticas laborales, los valores y las orientaciones de sus trabajadores.</li>
            <li>Evaluar la adecuación institucional o correspondencia entre los objetivos formales y su desempeño real.</li>
            <li>Evaluar las contribuciones al progreso económico, social o político de las instituciones en sus respectivas esferas de actividad.</li>
        </ul>

        <h5><b>¿Cómo estudiamos la Calidad Institucional?</b></h5>
        <p>
            El estudio parte de la investigación acumulada en la sociología y en la economía institucional. Utiliza una serie de hipótesis sobre <b>dimensiones de carácter interno o externo a cada institución que pueden influir en su funcionamiento y en sus resultados</b>:
        </p>

        <h5><b>Dimensiones Internas:</b></h5>
        <ul>
            <li>Meritocracia en el reclutamiento y en la promoción del personal.</li>
            <li>Inmunidad respecto a la presencia de intereses particularistas.</li>
            <li>Ausencia de "islas de poder" susceptibles de subvertir las reglas institucionales y desviarlas del cumplimiento de sus fines.</li>
        </ul>

        <h5><b>Dimensiones Externas:</b></h5>
        <ul>
            <li>"Proactividad" o capacidad para emprender actividades o relacionarse con otros actores con el objetivo de mejorar los servicios.</li>
            <li>Flexibilidad tecnológica y apertura a la innovación.</li>
            <li>Alianzas que ayuden al cumplimiento de objetivos.</li>
        </ul>

        <h5><b>Instituciones de referencia en este estudio:</b></h5>
        <ul>
            <li>Universidades.</li>
            <li>Organismos Públicos de Investigación.</li>
            <li>Centros de Investigación y Tecnología.</li>
        </ul>

        <h5><b>Resultados esperados</b></h5>
        <p>
            Esperamos <b>identificar problemas de utilidad</b> para la actuación de autoridades políticas y gestores y para el conocimiento de los ciudadanos, dirigidos a la mejora de la gobernanza del sistema de universidades e I+D.
        </p>
        <ul>
            <li>Informe general de resultados de carácter comparativo dirigido a explorar las dimensiones de la calidad institucional en los principales organismos del sistema.</li>
            <li>Libro con los resultados detallados de la investigación.</li>
            <li>Artículos y monográficos en revistas especializadas.</li>
            <li>Actividades de divulgación.</li>
        </ul>

